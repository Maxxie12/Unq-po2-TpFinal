package ar.edu.unq.po2.TPFinal.Common;

public class Coordenada {
	
	private Integer x;
	private Integer y;
	
	public Coordenada(Integer x, Integer y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	

}
